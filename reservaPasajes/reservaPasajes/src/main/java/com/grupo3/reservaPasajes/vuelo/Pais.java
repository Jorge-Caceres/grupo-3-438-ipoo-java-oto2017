package com.grupo3.reservaPasajes.vuelo;

public class Pais {
	
	private long id;
	private String nombre;
	
	public Pais(){
		
	}

	public long getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setId(long id) {
		this.id = id;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
